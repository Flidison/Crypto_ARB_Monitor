// эта функция find_alias решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// тут просто продолжаем собирать итоговый результат
static std::optional<size_t> find_alias(const std::unordered_map<std::string, size_t>& idx,
// на этом месте держим логику прямой и читаемой
                                        std::initializer_list<const char*> aliases) {
// запускаем цикл чтобы пройтись по всей коллекции
    for (const char* a : aliases) {
// эта функция find решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
        const auto it = idx.find(str::upper_copy(a));
// проверяем условие чтобы отсеять неподходящий сценарий
        if (it != idx.end()) return it->second;
// закрываем текущий блок и поднимаемся уровнем выше
    }
// возвращаем рассчитанный результат в вызывающий код
    return std::nullopt;
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция require_alias решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// тут закрываем небольшой кусок общей задачи
static size_t require_alias(const std::unordered_map<std::string, size_t>& idx,
// фиксируем значение чтобы дальше случайно его не менять
                            const char* logical_name,
// тут закрываем небольшой кусок общей задачи
                            std::initializer_list<const char*> aliases) {
// эта функция find_alias решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const auto c = find_alias(idx, aliases);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (!c.has_value()) throw CsvError(std::string("Missing crypto column: ") + logical_name);
// возвращаем рассчитанный результат в вызывающий код
    return *c;
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция csv_escape решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// этой строкой фиксируем промежуточный результат
static std::string csv_escape(const std::string& in) {
// сохраняем значение в переменную чтобы использовать дальше
    bool needs_quotes = false;
// делаем небольшой шаг и готовим почву для следующих строк
    std::string out;
// эта функция reserve решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
    out.reserve(in.size() + 4);
// запускаем цикл чтобы пройтись по всей коллекции
    for (const char ch : in) {
// проверяем условие чтобы отсеять неподходящий сценарий
        if (ch == '"' || ch == ',' || ch == '\n' || ch == '\r') needs_quotes = true;
// проверяем условие чтобы отсеять неподходящий сценарий
        if (ch == '"') out.push_back('"');
// эта функция push_back решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
        out.push_back(ch);
// закрываем текущий блок и поднимаемся уровнем выше
    }
// проверяем условие чтобы отсеять неподходящий сценарий
    if (!needs_quotes) return out;
// возвращаем рассчитанный результат в вызывающий код
    return "\"" + out + "\"";
// закрываем текущий блок и поднимаемся уровнем выше
}

