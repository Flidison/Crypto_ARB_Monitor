// эта функция append_profit_csv решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// этот кусок нужен чтобы код шел по понятному сценарию
double CryptoArbitrageEngine::append_profit_csv(
// фиксируем значение чтобы дальше случайно его не менять
    const std::string& path,
// фиксируем значение чтобы дальше случайно его не менять
    const std::string& observed_at,
// фиксируем значение чтобы дальше случайно его не менять
    const double capital_before,
// фиксируем значение чтобы дальше случайно его не менять
    const std::vector<CryptoOpportunity>& opps) const {

// эта функция out решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
    std::ofstream out(path, std::ios::app);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (!out) throw CsvError("Cannot append crypto profit report: " + path);
// эта функция setprecision решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// доклеиваем следующую часть данных в выходной поток
    out << std::fixed << std::setprecision(8);

// проверяем условие чтобы отсеять неподходящий сценарий
    if (opps.empty()) {
// эта функция csv_escape решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// доклеиваем следующую часть данных в выходной поток
        out << csv_escape(observed_at) << ","
// доклеиваем следующую часть данных в выходной поток
            << capital_before << ","
// доклеиваем следующую часть данных в выходной поток
            << 0 << ","
// доклеиваем следующую часть данных в выходной поток
            << ",,,"
// доклеиваем следующую часть данных в выходной поток
            << 0.0 << ","
// доклеиваем следующую часть данных в выходной поток
            << 0.0 << ","
// доклеиваем следующую часть данных в выходной поток
            << capital_before << "\n";
// возвращаем рассчитанный результат в вызывающий код
        return capital_before;
// закрываем текущий блок и поднимаемся уровнем выше
    }

// эта функция max_element решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const auto best_it = std::max_element(opps.begin(), opps.end(), [](const CryptoOpportunity& a, const CryptoOpportunity& b) {
// возвращаем рассчитанный результат в вызывающий код
        return a.net_pct < b.net_pct;
// делаем небольшой шаг и готовим почву для следующих строк
    });
// фиксируем значение чтобы дальше случайно его не менять
    const CryptoOpportunity& best = *best_it;
// фиксируем значение чтобы дальше случайно его не менять
    const double estimated_profit = capital_before * best.net_pct;
// фиксируем значение чтобы дальше случайно его не менять
    const double capital_after = capital_before + estimated_profit;

// эта функция csv_escape решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// доклеиваем следующую часть данных в выходной поток
    out << csv_escape(observed_at) << ","
// доклеиваем следующую часть данных в выходной поток
        << capital_before << ","
// эта функция size решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// доклеиваем следующую часть данных в выходной поток
        << opps.size() << ","
// эта функция csv_escape решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// доклеиваем следующую часть данных в выходной поток
        << csv_escape(best.symbol) << ","
// эта функция csv_escape решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// доклеиваем следующую часть данных в выходной поток
        << csv_escape(best.buy_exchange) << ","
// эта функция csv_escape решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// доклеиваем следующую часть данных в выходной поток
        << csv_escape(best.sell_exchange) << ","
// доклеиваем следующую часть данных в выходной поток
        << best.net_pct << ","
// доклеиваем следующую часть данных в выходной поток
        << estimated_profit << ","
// доклеиваем следующую часть данных в выходной поток
        << capital_after << "\n";
// возвращаем рассчитанный результат в вызывающий код
    return capital_after;
// закрываем текущий блок и поднимаемся уровнем выше
}

