// эта функция detect_delimiter решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// этой строкой фиксируем промежуточный результат
static char detect_delimiter(const std::string& header) {
// фиксируем значение чтобы дальше случайно его не менять
    const std::vector<char> candidates = {',', ';', '\t', '|'};
// сохраняем значение в переменную чтобы использовать дальше
    size_t best = 0;
// сохраняем значение в переменную чтобы использовать дальше
    char delimiter = ',';
// запускаем цикл чтобы пройтись по всей коллекции
    for (const char c : candidates) {
// эта функция count решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
        const size_t cnt = static_cast<size_t>(std::count(header.begin(), header.end(), c));
// проверяем условие чтобы отсеять неподходящий сценарий
        if (cnt > best) {
// сохраняем значение в переменную чтобы использовать дальше
            best = cnt;
// сохраняем значение в переменную чтобы использовать дальше
            delimiter = c;
// закрываем текущий блок и поднимаемся уровнем выше
        }
// закрываем текущий блок и поднимаемся уровнем выше
    }
// возвращаем рассчитанный результат в вызывающий код
    return delimiter;
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция split_csv_line решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// здесь аккуратно готовим данные для следующего шага
static std::vector<std::string> split_csv_line(const std::string& line, char delimiter) {
// делаем небольшой шаг и готовим почву для следующих строк
    std::vector<std::string> out;
// делаем небольшой шаг и готовим почву для следующих строк
    std::string cell;
// сохраняем значение в переменную чтобы использовать дальше
    bool in_quotes = false;
// запускаем цикл чтобы пройтись по всей коллекции
    for (size_t i = 0; i < line.size(); ++i) {
// фиксируем значение чтобы дальше случайно его не менять
        const char ch = line[i];
// проверяем условие чтобы отсеять неподходящий сценарий
        if (ch == '"') {
// проверяем условие чтобы отсеять неподходящий сценарий
            if (in_quotes && i + 1 < line.size() && line[i + 1] == '"') {
// эта функция push_back решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
                cell.push_back('"');
// делаем небольшой шаг и готовим почву для следующих строк
                ++i;
// на этом месте держим логику прямой и читаемой
            } else {
// сохраняем значение в переменную чтобы использовать дальше
                in_quotes = !in_quotes;
// закрываем текущий блок и поднимаемся уровнем выше
            }
// пропускаем остаток итерации и берем следующий элемент
            continue;
// закрываем текущий блок и поднимаемся уровнем выше
        }
// проверяем условие чтобы отсеять неподходящий сценарий
        if (ch == delimiter && !in_quotes) {
// эта функция push_back решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
            out.push_back(str::trim_copy(cell));
// эта функция clear решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
            cell.clear();
// пропускаем остаток итерации и берем следующий элемент
            continue;
// закрываем текущий блок и поднимаемся уровнем выше
        }
// эта функция push_back решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
        cell.push_back(ch);
// закрываем текущий блок и поднимаемся уровнем выше
    }
// проверяем условие чтобы отсеять неподходящий сценарий
    if (in_quotes) throw CsvError("Malformed CSV line in crypto quotes: unclosed quote");
// эта функция push_back решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// делаем небольшой шаг и готовим почву для следующих строк
    out.push_back(str::trim_copy(cell));
// возвращаем рассчитанный результат в вызывающий код
    return out;
// закрываем текущий блок и поднимаемся уровнем выше
}

// эта функция parse_double решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// тут просто продолжаем собирать итоговый результат
static double parse_double(const std::string& raw, const char* field_name) {
// эта функция trim_copy решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const std::string s = str::trim_copy(raw);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (s.empty()) throw DataValidationError(std::string("Empty numeric field: ") + field_name);
// сохраняем значение в переменную чтобы использовать дальше
    char* end = nullptr;
// эта функция strtod решает отдельную задачу внутри общего потока работы
// по шагам берем вход, делаем проверки и отдаем ожидаемый результат наружу
// фиксируем значение чтобы дальше случайно его не менять
    const double v = std::strtod(s.c_str(), &end);
// проверяем условие чтобы отсеять неподходящий сценарий
    if (end == s.c_str() || *end != '\0' || !std::isfinite(v)) {
// бросаем исключение чтобы явно подсветить ошибочный кейс
        throw DataValidationError(std::string("Bad numeric field: ") + field_name);
// закрываем текущий блок и поднимаемся уровнем выше
    }
// возвращаем рассчитанный результат в вызывающий код
    return v;
// закрываем текущий блок и поднимаемся уровнем выше
}

