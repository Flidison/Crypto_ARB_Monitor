// Пояснение к алгоритму.
// write_temp_csv: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Технический шаг.
std::string write_temp_csv(const std::string& name, const std::string& body) {
// Фиксируем ожидаемое значение.
    const std::string path = "tmp_" + name + ".csv";
// out: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    std::ofstream out(path, std::ios::binary);
// доклеиваем следующую часть данных в выходной поток
    out << body;
// close: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    out.close();
// Возвращаем вычисленное значение.
    return path;
// Конец текущего блока.
}

// read_file: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Продолжение общей логики.
std::string read_file(const std::string& path) {
// in: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// Подготовка к следующему шагу.
    std::ifstream in(path, std::ios::binary);
// Подготовка к следующему шагу.
    std::ostringstream ss;
// rdbuf: ключевой шаг текущего сценария.
// Путь данных: вход -> проверки -> результат.
// доклеиваем следующую часть данных в выходной поток
    ss << in.rdbuf();
// Возвращаем вычисленное значение.
    return ss.str();
// Конец текущего блока.
}

